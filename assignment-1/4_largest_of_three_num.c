#include <stdio.h>

int main()
{
    int a = 10, b = 2, c = 20;
    int largest = 0;
    if (largest < a)
    {
        largest = a;
    }
    if (largest < b)
    {
        largest = b;
    }
    if (largest < c)
    {
        largest = c;
    }
    printf("Largest number is %d", largest);

    return 0;
}